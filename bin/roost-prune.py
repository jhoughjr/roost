#!/usr/bin/env python3
"""Per-project build-artifact pruner — `roost prune`.

Each project (a repo under ROOST_PROJECTS_DIR, default ~/repos) is a "board"
here; this finds the regenerable build artifacts inside each and, on request,
deletes them. Dry-run by DEFAULT: it never removes anything unless you pass
--yes. Nothing outside a known artifact subdir of a project (or a listed global
cache) is ever touched — source is safe.

  roost prune                 # dry-run: every project's artifacts, biggest first
  roost prune <project>       # dry-run: just that project
  roost prune --yes [proj]    # actually delete (all projects, or one)
  roost prune --deep [...]    # also node_modules / .build (regenerable but slow
                              #   to restore) and global build caches
  roost prune --caches        # only the global caches (electron, npm, DerivedData)

Config (~/.roostrc): ROOST_PROJECTS_DIR (default ~/repos),
ROOST_PRUNE_EXTRA (colon-separated extra artifact dir names).
"""
import os
import shutil
import subprocess
import sys

# Build outputs safe to delete anywhere — regenerated by the next build, cheap.
ARTIFACT_DIRS = ["out", "dist", "build", ".next", ".parcel-cache",
                 "DerivedData", "target/release", "target/debug"]
# Regenerable but expensive to restore (reinstall / recompile) — only with --deep.
DEEP_DIRS = ["node_modules", ".build", ".swiftpm", "Pods"]
# Host-level caches (not per project). Deleting them is safe; they refill.
GLOBAL_CACHES = [
    "~/Library/Caches/electron",
    "~/Library/Caches/electron-builder",
    "~/Library/Developer/Xcode/DerivedData",
    "~/.npm/_cacache",
    "~/Library/Caches/ms-playwright",
]


def rc(key, default=""):
    try:
        for line in open(os.path.expanduser("~/.roostrc")):
            line = line.strip()
            if line.startswith(f"{key}=") and not line.startswith("#"):
                return os.path.expandvars(line.split("=", 1)[1].strip())
    except OSError:
        pass
    return default


def du_bytes(path):
    """Apparent disk usage of a path in bytes (fast: shells out to du -sk)."""
    try:
        out = subprocess.run(["du", "-sk", path], capture_output=True, text=True).stdout
        return int(out.split("\t")[0]) * 1024 if out.strip() else 0
    except (ValueError, OSError):
        return 0


def human(n):
    for unit in ("B", "K", "M", "G", "T"):
        if n < 1024 or unit == "T":
            return f"{n:.0f}{unit}" if unit == "B" else f"{n:.1f}{unit}"
        n /= 1024


def find_targets(projects_dir, artifact_dirs, deep):
    """[(label, path, bytes)] for every artifact dir found under each project."""
    dirs = artifact_dirs + (DEEP_DIRS if deep else [])
    targets = []
    if os.path.isdir(projects_dir):
        for proj in sorted(os.listdir(projects_dir)):
            proot = os.path.join(projects_dir, proj)
            if not os.path.isdir(os.path.join(proot, ".git")):
                continue
            for d in dirs:
                p = os.path.join(proot, d)
                if os.path.isdir(p) and not os.path.islink(p):
                    targets.append((f"{proj}/{d}", p, du_bytes(p)))
    return targets


def find_caches():
    out = []
    for c in GLOBAL_CACHES:
        p = os.path.expanduser(c)
        if os.path.isdir(p) and not os.path.islink(p):
            out.append((c, p, du_bytes(p)))
    return out


def main(argv):
    apply = "--yes" in argv
    deep = "--deep" in argv
    caches_only = "--caches" in argv
    only = next((a for a in argv if not a.startswith("-")), None)

    projects_dir = os.environ.get("ROOST_PROJECTS_DIR") \
        or os.path.expanduser(rc("ROOST_PROJECTS_DIR", "~/repos"))
    extra = [d for d in rc("ROOST_PRUNE_EXTRA").split(":") if d]
    artifact_dirs = ARTIFACT_DIRS + extra

    if caches_only:
        targets = find_caches()
    else:
        targets = find_targets(projects_dir, artifact_dirs, deep)
        if deep:
            targets += find_caches()
    if only:
        targets = [t for t in targets if t[0].split("/")[0] == only or only in t[0]]

    targets = [t for t in targets if t[2] > 0]
    targets.sort(key=lambda t: t[2], reverse=True)
    total = sum(t[2] for t in targets)

    if not targets:
        print(f"prune: nothing to reclaim under {projects_dir}"
              + ("" if deep else " (try --deep for node_modules/.build, or --caches)"))
        return 0

    verb = "Deleting" if apply else "Would free"
    print(f"{verb} {human(total)} across {len(targets)} target(s)"
          + ("" if apply else "  — dry run, nothing deleted; pass --yes to apply") + ":\n")
    freed = 0
    for label, path, size in targets:
        mark = " "
        if apply:
            try:
                shutil.rmtree(path)
                freed += size
                mark = "✓"
            except OSError as e:
                mark = "✗"
                print(f"  ✗ {human(size):>7}  {label}  ({e})")
                continue
        print(f"  {mark} {human(size):>7}  {label}")
    print()
    if apply:
        print(f"✓ reclaimed {human(freed)}")
    else:
        print(f"Total reclaimable: {human(total)}   ·   `roost prune --yes"
              + (f" {only}" if only else "") + ("` --deep" if deep else "`") + " to delete")
    return 0


if __name__ == "__main__":
    sys.exit(main(sys.argv[1:]))
